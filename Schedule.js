export default function Schedule({ onBack }) {
  const [group, setGroup] = useState("ИВТ");
  const [course, setCourse] = useState(2); // ИВТ 1-курсу жок болгондуктан, 2-курстан башталат

  const data = SCHEDULE[group]?.[course] || {};

  return (
    <div>
      <h2 style={Styles.title}>Расписание занятий</h2>

      <div style={{ marginBottom: "20px" }}>
        <select value={group} onChange={e => setGroup(e.target.value)} style={Styles.select}>
          <option value="ИВТ">ИВТ</option>
          <option value="ПИЭ">ПИЭ</option>
        </select>
        <select value={course} onChange={e => setCourse(Number(e.target.value))} style={Styles.select}>
          {group === "ИВТ" ? (
            <>
              <option value={2}>2 курс</option>
              <option value={3}>3 курс</option>
              <option value={4}>4 курс</option>
            </>
          ) : (
            <>
              <option value={1}>1 курс</option>
              <option value={2}>2 курс</option>
              <option value={3}>3 курс</option>
              <option value={4}>4 курс</option>
            </>
          )}
        </select>
      </div>

      {Object.keys(data).length === 0 ? (
        <p style={Styles.error}>Расписание не найдено</p>
      ) : (
        Object.entries(data).map(([day, lessons], i) => (
          <div key={i} style={{ marginBottom: "30px" }}>
            <h3 style={Styles.subtitle}>{day}</h3>
            <table style={Styles.tableSmall}>
              <thead>
                <tr>
                  <th style={Styles.thSmall}>Время</th>
                  <th style={Styles.thSmall}>Предмет</th>
                  <th style={Styles.thSmall}>Аудитория</th>
                  <th style={Styles.thSmall}>Преподаватель</th>
                </tr>
              </thead>
              <tbody>
                {lessons.map((l, idx) => (
                  <tr key={idx}>
                    <td style={Styles.tdSmall}>{l.time}</td>
                    <td style={Styles.tdSmall}>{l.subject}</td>
                    <td style={Styles.tdSmall}>{l.room}</td>
                    <td style={Styles.tdSmall}>{l.teacher}</td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        ))
      )}

      <button style={Styles.backButton} onClick={onBack}>Назад</button>
    </div>
  );
}